#include <stdio.h>

/**
 * add - adds two intergers
 * a: Int 1
 * b: int 2
 * Return: Addition
 */
int add(int a, int b)
{	
return (a + b);
}

/**
 * sub - Subtracts two integers 
 * a: Int 1
 * b: Int 2
 * Return: Substraction
 */
int sub(int a, int b)
{
return (a - b);
}

/**
 * mul - multiplies two integers
 * a: int 1
 * b: int 2
 * Return: Multiplication
 */

int mul(int a, int b)
{
return (a * b);
}

/**
 * mod - mod of two integers
 * a: Int 1
 * b: Int 2
 * Return: Addition
 */
int mod(int a, int b)
{
return (a % b);
}

/**
 * div - Divides two integers
 * a: int 1
 * b: int 2
 * Return: Division
 */
int div(int a, int b)
{
return (a / b);
}
